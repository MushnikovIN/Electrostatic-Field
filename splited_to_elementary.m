function elementary_wires = splited_to_elementary(Wires)
    %SPLITED_TO_ELEMENTARY Преобразование расщепленных проводов в элементарные.
    %   ELEMENTARY_WIRES = SPLITED_TO_ELEMENTARY(WIRES) принимает структуру
    %   с параметрами линий электропередачи и раскладывает каждую расщепленную
    %   фазу (где is_splited = true) на N отдельных элементарных проводников,
    %   расположенных по окружности шага расщепления.
    %
    %   Особенности работы функции:
    %       - Геометрия расщепления рассчитывается тригонометрически: первый
    %         провод всегда располагается снизу, остальные — с шагом 360/N градусов.
    %       - Поле 'show_label' принимает значение true только для одиночных
    %         (нерасщепленных) проводов и для одного самого верхнего проводника
    %         в каждой группе расщепленных для исключения дублирования подписей.
    %
    %   Входные аргументы:
    %       WIRES - Исходный массив структур со свойствами фаз. Обязательные поля:
    %               .x, .y - координаты центра расщепленного провода (м).
    %               .U     - потенциал (В).
    %               .r0    - радиус элементарного проводника (м).
    %               .is_splited - флаг наличия расщепления (logical).
    %               .N     - число элементарных проводов в фазе.
    %               .d     - шаг расщепления (м).
    %
    %   Выходные аргументы:
    %       ELEMENTARY_WIRES - Массив структур элементарных проводников.
    %                          Содержит поля: .x, .y, .U, .r0, .show_label.


    % Инициализация пустого массива для сбора результирующих элементарных проводов
    elementary_wires = struct('x', {}, 'y', {}, 'U', {}, 'r0', {}, 'show_label', {});
    
    % Поэлементный перебор исходного набора проводников
    for i = 1:length(Wires)
        
        % Проверка условий расщепления проводника
        if Wires(i).is_splited && Wires(i).N > 1
            % Расчет радиуса окружности расщепления (rn)
            rn =  Wires(i).d / (2 * sin(pi / Wires(i).N));
            
            % Расчет углового шага распределения для каждого суб-проводника
            angles = -pi/2 + (0:Wires(i).N-1) * (2 * pi / Wires(i).N);
            
            % Расчет координат центров всех N суб-проводников одновременно
            sub_x = Wires(i).x + rn * cos(angles);
            sub_y = Wires(i).y + rn * sin(angles);

            % Поиск индекса суб-проводника с максимальной координатой Y
            [~, max_y_idx] = max(sub_y);

            % Добавление всех элементов расщепленной группы в общий массив
            for k = 1:Wires(i).N
                elementary_wires(end+1) = struct(...
                    'x', sub_x(k), ...
                    'y', sub_y(k), ...
                    'U', Wires(i).U, ...
                    'r0', Wires(i).r0, ...
                    'show_label', k == max_y_idx); % true только для самого верхнего провода в группе
            end
            
        else
            % Перенос нерасщепленного или одиночного проводника без изменений
            elementary_wires(end+1) = struct(...
                'x', Wires(i).x, ...
                'y', Wires(i).y, ...
                'U', Wires(i).U, ...
                'r0', Wires(i).r0, ...
                'show_label', true);  % Одиночный провод всегда отображает метку
        end
    end
end
