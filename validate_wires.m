function validate_wires(Wires)
    %VALIDATE_WIRES Первичная проверка структуры входных данных системы проводов.
    %   VALIDATE_WIRES(WIRES) выполняет анализ исходного массива структур WIRES 
    %   перед процедурой расщепления фаз. При обнаружении некорректных значений 
    %   функция генерирует ошибку и прерывает выполнение программы.
    %
    %   Проверяемые правила и критерии:
    %       - Тип входного аргумента должен быть структурой (struct).
    %       - Наличие всех обязательных полей конфигурации.
    %       - Координата 'y' должна быть строго положительной (выше земли).
    %       - Радиус проводника 'r0' должен быть строго больше нуля.
    %       - При 'is_splited' = true число элементов 'N' должно быть больше 1,
    %         а шаг расщепления 'd' — строго больше нуля.
    %
    %   Входные аргументы:
    %       WIRES - Исходный массив структур, описывающий конфигурацию проводов.

    % Проверка соответствия входного типа данных типу "структура"
    if ~isstruct(Wires)
        error('Входные данные должны быть структурой (struct).');
    end

    % Список обязательных полей для верификации
    required_fields = {'x', 'y', 'U', 'is_splited', 'r0', 'N', 'd'};
    for f = 1:length(required_fields)
        if ~isfield(Wires, required_fields{f})
            error('В структуре отсутствует обязательное поле: "%s".', required_fields{f});
        end
    end

    % Поэлементный перебор проводников для проверки физических и логических условий
    for i = 1:length(Wires)
       

        % Проверка числовых полей на соответствие типу данных
        numeric_fields = {'x', 'y', 'U', 'r0', 'N', 'd'};
        for f = 1:length(numeric_fields)
            field_name = numeric_fields{f};
            if ~isnumeric(Wires(i).(field_name))
                error('Ошибка в проводе №%d: поле "%s" должно быть числового типа (текущий тип: %s).', ...
                    i, field_name, class(Wires(i).(field_name)));
            end
        end
        
        % Проверка поля флага расщепления на соответствие логическому типу данных
        if ~islogical(Wires(i).is_splited)
            error('Ошибка в проводе №%d: поле "is_splited" должно быть логического типа (logical) (текущий тип: %s).', ...
                i, class(Wires(i).is_splited));
        end

        % Проверка координаты Y на строгое нахождение выше уровня земли
        if isempty(Wires(i).y) || isnan(Wires(i).y) || Wires(i).y <= 0
            error('Ошибка в проводе №%d: координата "y" (текущее значение: %s) должна быть строго больше 0.', ...
                i, num2str(Wires(i).y));
        end
        
        % Проверка радиуса элементарного проводника r0 на физическую корректность
        if isempty(Wires(i).r0) || isnan(Wires(i).r0) || Wires(i).r0 <= 0
            error('Ошибка в проводе №%d: радиус "r0" (текущее значение: %s) должен быть строго больше 0.', ...
                i, num2str(Wires(i).r0));
        end
        
        % Проверка логической связности параметров расщепления при активном флаге
        if islogical(Wires(i).is_splited) && Wires(i).is_splited
           
            % Проверка числа проводников в группе расщепления N
            if isempty(Wires(i).N) || isnan(Wires(i).N) || Wires(i).N <= 1
                error('Ошибка в проводе №%d: для расщепленного провода (is_splited = true) число элементов "N" должно быть больше 1 (текущее: %s).', ...
                    i, num2str(Wires(i).N));
            end
            
            % Проверка шага геометрического расщепления d
            if isempty(Wires(i).d) || isnan(Wires(i).d) || Wires(i).d <= 0
                error('Ошибка в проводе №%d: для расщепленного провода (is_splited = true) шаг "d" должен быть строго больше 0 (текущее: %s).', ...
                    i, num2str(Wires(i).d));
            end
        end
    end
    
    % Вывод уведомления об успешном завершении всех проверок в командное окно
    fprintf('Валидация структуры Wires успешно пройдена. Ошибок не обнаружено.\n');
end
